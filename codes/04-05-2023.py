def get_floor(commands: str):
    up_comms, down_comms = [], []
    for char in commands:
        if char == '<':
            up_comms.append(char)
        elif char == '>':
            down_comms.append(char)
    l_up, l_down = len(up_comms), len(down_comms)
    diff = l_up - l_down
    print(f"Floor {diff}")


if __name__ == '__main__':
    while True:
        commands = input('Commands: ')
        get_floor(commands)